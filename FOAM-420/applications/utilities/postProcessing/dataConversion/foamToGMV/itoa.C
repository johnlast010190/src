#include "itoa.H"
#include "primitives/ints/uLabel/uLabel.H"

// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

namespace Foam
{

// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

word itoa(const label n)
{
    const label offset = '0';
    const label length = 3;

    char val[length + 1];

    label leftOfN = n;

    for (label i=0; i<length; i++)
    {
        // Explicit instantiation of RHS components as uLabels is necessary to
        // avoid ambiguity in the Unity Int64 builds.
        label j = label(leftOfN/pow(uLabel(10), uLabel(length-i-1)));
        leftOfN -= j*pow(uLabel(10), uLabel(length-i-1));
        val[i] = offset + j;
    }

    val[length] = 0;

    return val;
}


// * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * //

} // End namespace Foam

