/*---------------------------------------------------------------------------*\
                           Class faPatchData Declaration
\*---------------------------------------------------------------------------*/

struct faPatchData
{
    word name_;
    word type_;
    dictionary dict_;
    label ownPolyPatchID_;
    label ngbPolyPatchID_;
    labelList edgeLabels_;
    faPatchData()
    :
        name_(word::null),
        type_(word::null),
        ownPolyPatchID_(-1),
        ngbPolyPatchID_(-1)
    {}
};


